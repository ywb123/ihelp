package domin;

public class goldbeanlevel {
	private int id;
	private String goldbeanlevel;
	
	public goldbeanlevel(int id, String goldbeanlevel) {
		super();
		this.id = id;
		this.goldbeanlevel = goldbeanlevel;
	}

	public goldbeanlevel() {	
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getGoldbeanlevel() {
		return goldbeanlevel;
	}

	public void setGoldbeanlevel(String goldbeanlevel) {
		this.goldbeanlevel = goldbeanlevel;
	}
}
