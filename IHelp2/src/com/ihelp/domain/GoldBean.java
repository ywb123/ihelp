package com.ihelp.domain;

public class GoldBean {

	private int number;
	private String topic;
	public GoldBean(){}
	
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public String getTopic() {
		return topic;
	}
	public void setTopic(String topic) {
		this.topic = topic;
	}
	
}
